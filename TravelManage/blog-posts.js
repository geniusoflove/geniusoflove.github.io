// Initialize blog display on page load
document.addEventListener("DOMContentLoaded", () => {
  // ===== Home Blog Section (3 featured posts from BLOG_POSTS) =====
  const homeBlogGrid = document.getElementById("homeBlogGrid");

  if (homeBlogGrid && Array.isArray(BLOG_POSTS) && BLOG_POSTS.length > 0) {
    
    const posts = [...BLOG_POSTS].sort(
      (a, b) => new Date(b.date) - new Date(a.date)
    );

    // Filter by category
    const destinationPosts = posts.filter(p =>
      (p.category || "").toLowerCase().includes("destination")
    );

    const tipPosts = posts.filter(p =>
      (p.category || "").toLowerCase().includes("travel tips")
    );

    // Pick posts for required order:
    const selectedPosts = [
      destinationPosts[0], // First Destination
      tipPosts[0],         // One Travel Tips Post
      destinationPosts[1]  // Next Destination
    ].filter(Boolean); // Remove any null entries if fewer posts exist

    function getTagInfo(post) {
      const cat = (post.category || "").toLowerCase();
      if (cat.includes("travel tips")) {
        return { className: "tag-tip", label: "Travel Tips" };
      }
      if (cat.includes("destination")) {
        return { className: "tag-dest", label: "Destination Picks" };
      }
      return { className: "tag-news", label: post.category || "Travel Blog" };
    }

    homeBlogGrid.innerHTML = "";

    selectedPosts.forEach((post) => {
      const { className, label } = getTagInfo(post);
      const article = document.createElement("article");
      article.className = "card blog-card";

      article.innerHTML = `
        <div class="card-tag ${className}">${label}</div>
        <img src="${post.image}" alt="${post.imageAlt || post.title}" class="card-image" />
        <h3>${post.title}</h3>
        <p>${post.excerpt || post.intro || ""}</p>
        <a href="blog-post.html?id=${encodeURIComponent(post.id)}" class="card-link">
          Read More
        </a>
      `;

      homeBlogGrid.appendChild(article);
    });
  }
});
